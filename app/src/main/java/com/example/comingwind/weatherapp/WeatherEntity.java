    public void setNight_fengxiang(String night_fengxiang) {
        this.night_fengxiang = night_fengxiang;
    }

    public String getNight_fengli() {
        return night_fengli;
    }

    public void setNight_fengli(String night_fengli) {
        this.night_fengli = night_fengli;
    }
